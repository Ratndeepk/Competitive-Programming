#include<bits/stdc++.h>
using namespace std;
#define MOD 1337
long long power(long long x,long long y) 
{ 
	if (y == 0) 
		return 1; 
	else if (y % 2 == 0) 
		return ((power(x, y / 2)%MOD) * (power(x, y / 2)%MOD)%MOD); 
	else
		return ((x%MOD) *(power(x, y / 2)%MOD) * (power(x, y / 2)%MOD)%MOD); 
}

int conNum(int arr[],int n)
{
	int num=0;
	int m=1;
	for(int i=n-1;i>=0;i--)
	{
		num+=(arr[i]*m);
		m*=10;
	}
	return num;
}
int main()
{
	int a,n;
	cin>>a>>n;
	
	int arr[n];
	
	for(int i=0;i<n;i++)
	{
		cin>>arr[i];
	}
	
	int pow=conNum(arr,n);
	
	int ans=power(a,pow);
	
	cout<<ans<<"\n";
	
	return 0;
}


